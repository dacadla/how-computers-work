        <h1>Different computer hardware components</h1>
        <p>Your computer is made up of many different parts. Most or all of these parts are needed for your computer to run, or give extra features such as reading optical discs. Some parts aim to help other parts do their job, such as cooling systems.</p>
        <p>All of these components work together to run your computer, with the help of an "orchestrator" in software so other software can use the hardware to it's full potential.</p>
        <p>The following are some components and a short description of them:</p>
        <br>
        <section>
            <h2>Central Processing Unit (CPU)</h2>
            <p>
                The CPU is the brain of the computer and performs most of the processing. It executes instructions and carries out calculations.
            </p>
        </section>

        <section>
            <h2>Motherboard</h2>
            <p>
                The motherboard is the main circuit board of the PC. It houses the CPU, RAM, and other essential components. It also provides connectors for additional hardware components.
            </p>
        </section>

        <section>
            <h2>Random Access Memory (RAM)</h2>
            <p>
                RAM is the computer's short-term memory. It stores data and instructions that the CPU needs to access quickly while running programs.
            </p>
        </section>

        <section>
            <h2>Hard Disk Drive (HDD) or Solid-State Drive (SSD)</h2>
            <p>
                These are storage devices that store your operating system, software, and data. HDDs use spinning disks to store data, while SSDs use flash memory for faster access.
            </p>
        </section>

        <section>
            <h2>Graphics Processing Unit (GPU)</h2>
            <p>
                The GPU is responsible for rendering graphics and images. In addition to gaming and graphics tasks, modern GPUs also assist with certain computational tasks like machine learning.
            </p>
        </section>

        <section>
            <h2>Power Supply Unit (PSU)</h2>
            <p>
                The PSU provides electrical power to the components of the computer. It converts AC power from your wall outlet into DC power usable by the computer.
            </p>
        </section>

        <section>
            <h2>Optical Drive</h2>
            <p>
                While becoming less common, optical drives like DVD or Blu-ray drives are used for reading and writing optical discs.
            </p>
        </section>

        <section>
            <h2>Cooling System</h2>
            <p>
                To prevent overheating, PCs include cooling components such as fans, heat sinks, and sometimes liquid cooling systems.
            </p>
        </section>

        <section>
            <h2>Case (Chassis)</h2>
            <p>
                The computer case holds and protects all the internal components. It also provides ventilation and houses the power button, USB ports, and other connectors.
            </p>
        </section>

        <section>
            <h2>Input/Output Ports</h2>
            <p>
                These are used to connect peripherals to the computer, such as USB ports, audio jacks, HDMI, DisplayPort, Ethernet, and more.
            </p>
        </section>

        <section>
            <h2>Network Interface Card (NIC)</h2>
            <p>
                This component provides wired or wireless network connectivity.
            </p>
        </section>

        <section>
            <h2>Expansion Cards</h2>
            <p>
                You can add functionality to your PC by installing expansion cards. Common types include graphics cards, sound cards, and network cards.
            </p>
        </section>

        <section>
            <h2>Peripherals</h2>
            <p>
                These are external devices like keyboards, mice, monitors, printers, and speakers that you connect to your PC for input and output.
            </p>
        </section>

        <section>
            <h2>Power Cables and Data Cables</h2>
            <p>
                These cables connect the components and peripherals to the power supply and motherboard, enabling data transfer and power distribution.
            </p>
        </section>

        <section>
            <h2>BIOS/UEFI Chip</h2>
            <p>
                This is a chip on the motherboard that contains firmware, which initializes hardware components during the boot-up process.
            </p>
        </section>

